let a = [
    "naranja",
    "manzana",
    "pera"
];


a.push("uva"); // agregar un elemento al array

alert(a.pop()); // te devuelve el último elemento del array y después te lo elimina

a.shift(); // te devuelve el primer elemento del array y después lo elimina

a.unshift("uva") // te agrega un elemento al principio del array


// *** Rápidos ***
// push
// pop

// *** Lentos ***
// unshift
// shift